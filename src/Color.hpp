struct Color
{
	int R,G,B;
	Color():
		R(0),G(0),B(0){}
	Color(int col)
	{
		if(col==-1)	R=-1;
		else
		{
			R=(col&0xff0000)>>16;
			G=(col&0x00ff00)>>8;
			B=(col&0x0000ff);
		}
	}
	int toInt(){return B|(G<<8)|(R<<16);}
	bool DontModify(){return R==-1;}
};

