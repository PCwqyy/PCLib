Color RGB(int R,int G,int B)
{
	Color ret;
	ret.R=R,ret.G=G,ret.B=B;
	return ret;
}
Color HSL(int H,int S,int L)
{
	double r,g,b;
	double h=H/360.0;
	double s=S/100.0;
	double l=L/100.0;
	auto hue2rgb=[](double p,double q,double t)
	{
		if(t<0)	t++;
		if(t>1)	t--;
		if(t<1.0/6)	return p+(q-p)*6*t;
		if(t<1.0/2)	return q;
		if(t<2.0/3)	return p+(q-p)*(2.0/3-t)*6;
		return p;
	};
	if(s==0)	r=g=b=l;
	else
	{
		double q=l<0.5?l*(1+s):l+s-l*s;
		double p=2*l-q;
		r=hue2rgb(p,q,h+1.0/3);
		g=hue2rgb(p,q,h);
		b=hue2rgb(p,q,h-1.0/3);
	}
	Color ret;
	ret.R=int(r*255);
	ret.G=int(g*255);
	ret.B=int(b*255);
	return ret;
}

Color AverageColor(Color Col1,Color Col2,double index=0.5)
{
	Color ret;
	ret.R=int(Col1.R*index+Col2.R*(1-index));
	ret.G=int(Col1.G*index+Col2.G*(1-index));
	ret.B=int(Col1.B*index+Col2.B*(1-index));
	return ret;
}
Color HighContrust(Color col,double index=0.5)
	{return AverageColor(col,0xffffff,index);}

Color InvertColor(Color col)
	{return RGB(255-col.R,255-col.G,255-col.B);}

